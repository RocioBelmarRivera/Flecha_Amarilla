    private void limpiar_campos() {
        jTNombre.setText("");
        jTFPorcentaje.setText("");
      
    }

    private boolean campos_vacios() {
        return jTNombre.getText().isEmpty()
       || jTFPorcentaje.getText().isEmpty() ;

    }

    private void limpiar_tabla() {
        DefaultTableModel modelTabla
                = (DefaultTableModel) jTTipo.getModel();
        for (int i = (modelTabla.getRowCount() - 1); i >= 0; i--) {
            modelTabla.removeRow(i);
        }
    }

    private void lee_datos() {
        numero = 1;
        //2.- obtener modelo de la tabla de datos
        DefaultTableModel modelTabla
                = (DefaultTableModel) jTTipo.getModel();
        try {
            //3. leer los dtos
            resultados = modelos.busca_objetos_model();
            limpiar_tabla();
            //asignar dtos a la tabla
            for (String[] resultado : resultados) {
                //a√±adir datos al modelo de la tabla
                modelTabla.addRow(new Object[]{
                    resultado[0],
                    resultado[1],
                    resultado[2]
                });
            }
        } catch (SQLException e) {
        }
    }
    
    private int lee_fila_seleccionada() {
        int id = -1;
        DefaultTableModel modelTabla
                = (DefaultTableModel) jTTipo.getModel();
        if (modelTabla.getRowCount() != 0) {//TABLA CON FILAS
            if (jTTipo.getSelectedRow() != -1) {
                id = Integer.parseInt((String) modelTabla.getValueAt(jTTipo.getSelectedRow(), 0));
                // System.out.println(" " + idEliminar);//prueba
                jTNombre.setText((String) modelTabla.getValueAt(jTTipo.getSelectedRow(), 1));
                jTFPorcentaje.setText((String)modelTabla.getValueAt(jTTipo.getSelectedRow(),2));

            } else {
            }
        } else {//SI LA TABLA NO TIENE FILAS
            CUtilitarios.msg_adver("TABLA VACIA", "ELIMINA DATO");
        }
        return id;
    }

    private void actualiza_dato()  {
        int id;
       // int porcentaje;
        DefaultTableModel modelTabla
                = (DefaultTableModel)jTTipo.getModel();
        if (campos_vacios()) {
            CUtilitarios.msg_adver("Datos vacios", "Actualiza datos");
        } else {
            try {
                id = Integer.parseInt((String) modelTabla.getValueAt(jTTipo.getSelectedRow(), 0));
                String nombre = jTNombre.getText();
               int porcentaje = Integer.parseInt((String) modelTabla.getValueAt(jTTipo.getSelectedRow(),2));
                modelos.actualiza_objeto_model(id, nombre, porcentaje);
                lee_datos();
            } catch (Exception e) {
            }
        }
        limpiar_campos();
    }


